import { useState } from 'react';
import { useNavigate, Link } from 'react-router-dom';
import { Button } from '../components/ui/button';
import { Input } from '../components/ui/input';
import { Label } from '../components/ui/label';
import { Card, CardContent } from '../components/ui/card';
import { toast } from 'sonner';
import api from '../utils/api';
import { Mail, Lock, User } from 'lucide-react';
import BrandLogo from '../components/BrandLogo';
import { isDevAuthEnabled, devSignIn } from '../utils/devAuth';

const Register = () => {
  const navigate = useNavigate();
  const [name, setName] = useState('');
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [loading, setLoading] = useState(false);

  const handleRegister = async (e) => {
    e.preventDefault();
    setLoading(true);

    try {
      const response = await api.post('/auth/register', { name, email, password });
      localStorage.setItem('token', response.data.access_token);
      localStorage.setItem('user', JSON.stringify(response.data.user));
      toast.success('Account created successfully!');
      navigate('/projects');
    } catch (error) {
      const msg = error.response?.data?.detail || error.message || 'Registration failed';
      if (isDevAuthEnabled()) {
        const { user } = devSignIn({ email, name });
        toast.success(`Dev account created: ${user.name}`);
        navigate('/projects');
      } else {
        toast.error(msg);
      }
    } finally {
      setLoading(false);
    }
  };

  const handleGoogleSignup = async () => {
    try {
      const response = await api.get('/auth/google');
      if (response.data.auth_url) {
        window.location.href = response.data.auth_url;
      } else {
        toast.error(response.data.message || 'Google OAuth not configured.');
      }
    } catch (err) {
      if (isDevAuthEnabled()) {
        const { user } = devSignIn({ email: 'dev-google@example.com', name: 'Dev Google' });
        toast.success(`Dev Google sign-up: ${user.name}`);
        navigate('/projects');
      } else {
        toast.error('Failed to initiate Google OAuth. Network Error');
      }
    }
  };

  return (
    <div className="min-h-screen relative bg-gradient-to-br from-[#0b0b0d] via-[#0f172a] to-[#1a1030]">
      <div className="absolute inset-0 pointer-events-none opacity-10"
           style={{ backgroundImage: 'radial-gradient(circle at 20% 20%, #9b00e8 0%, transparent 25%), radial-gradient(circle at 80% 30%, #ff4500 0%, transparent 25%)' }} />
      <div className="min-h-screen flex items-center justify-center px-4">
        <div className="w-full max-w-md">
          <div className="flex flex-col items-center mb-6">
            <BrandLogo className="h-16 w-16 rounded-lg shadow-lg" />
            <p className="mt-3 text-sm text-slate-300">Join thousands building with stealth and precision</p>
          </div>

          <Card className="border border-white/10 bg-white/5 backdrop-blur-md shadow-xl">
            <CardContent className="pt-6">
              <form onSubmit={handleRegister} className="space-y-4">
                <div className="space-y-2">
                  <Label htmlFor="name" className="text-slate-200">Full Name</Label>
                  <div className="relative">
                    <User className="absolute left-3 top-3 h-4 w-4 text-slate-400" />
                    <Input
                      id="name"
                      type="text"
                      placeholder="John Doe"
                      value={name}
                      onChange={(e) => setName(e.target.value)}
                      className="pl-10 bg:white/5 bg-white/5 border-slate-600 text-white placeholder:text-slate-400"
                      required
                      data-testid="name-input"
                    />
                  </div>
                </div>

                <div className="space-y-2">
                  <Label htmlFor="email" className="text-slate-200">Email</Label>
                  <div className="relative">
                    <Mail className="absolute left-3 top-3 h-4 w-4 text-slate-400" />
                    <Input
                      id="email"
                      type="email"
                      placeholder="you@example.com"
                      value={email}
                      onChange={(e) => setEmail(e.target.value)}
                      className="pl-10 bg-white/5 border-slate-600 text-white placeholder:text-slate-400"
                      required
                      data-testid="email-input"
                    />
                  </div>
                </div>

                <div className="space-y-2">
                  <Label htmlFor="password" className="text-slate-200">Password</Label>
                  <div className="relative">
                    <Lock className="absolute left-3 top-3 h-4 w-4 text-slate-400" />
                    <Input
                      id="password"
                      type="password"
                      placeholder="••••••••"
                      value={password}
                      onChange={(e) => setPassword(e.target.value)}
                      className="pl-10 bg-white/5 border-slate-600 text-white placeholder:text-slate-400"
                      required
                      minLength={6}
                      data-testid="password-input"
                    />
                  </div>
                  <p className="text-xs text-slate-400">Must be at least 6 characters</p>
                </div>

                <Button type="submit" className="w-full bg-gradient-to-r from-[#9b00e8] to-[#ff4500] hover:from-[#8800d4] hover:to-[#e63e00]" disabled={loading} data-testid="register-button">
                  {loading ? 'Creating account...' : 'Create account'}
                </Button>
              </form>

              <div className="relative my-6">
                <div className="absolute inset-0 flex items-center">
                  <div className="w-full border-t border-white/10"></div>
                </div>
                <div className="relative flex justify-center text-xs uppercase">
                  <span className="bg-transparent px-2 text-slate-400">Or continue with</span>
                </div>
              </div>

              <Button type="button" variant="outline" className="w-full border-slate-600 text-white hover:bg-white/10" onClick={handleGoogleSignup} data-testid="google-signup-button">
                <svg className="mr-2 h-4 w-4" viewBox="0 0 24 24">
                  <path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" fill="#4285F4"/>
                  <path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" fill="#34A853"/>
                  <path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" fill="#FBBC05"/>
                  <path d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" fill="#EA4335"/>
                </svg>
                Sign up with Google
              </Button>

              <p className="mt-6 text-center text-sm text-slate-400">
                Already have an account?{' '}
                <Link to="/login" className="font-medium text-white hover:underline">
                  Sign in
                </Link>
              </p>
            </CardContent>
          </Card>
        </div>
      </div>
    </div>
  );
};

export default Register;